# time complexities -------------
# constant time - same time and space despite size
# linear time - bigger the range, the bigger the running time!
# logarithmic time - running time of input to get the num of operations
# quadratic time - linear operation of each value of input data, squared
# exponential time - doubles with each iteration - fibonacci sequence

